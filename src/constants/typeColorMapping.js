/**
 * 获取签文类型对应的颜色
 * @param {string} type - 签文类型
 * @param {string} defaultColor - 默认颜色，如果类型不存在
 * @returns {string} 对应的颜色代码
 */
export const getTypeColor = (type, defaultColor = '#333333') => {
  return typeColorMapping[type] || defaultColor;
};

/**
 * 获取签文级别
 * @param {string} type - 签文类型
 * @returns {string} 级别描述
 */
export const getTypeLevel = (type) => {
  if (['上上大吉', '天作之合', '缘定三生'].includes(type)) {
    return '殊胜';
  }
  if (['大吉', '良缘近', '事业顺遂', '学业精进'].includes(type)) {
    return '极好';
  }
  if (['中吉', '缘初现', '待花开', '贵人相助', '日行一善'].includes(type)) {
    return '良好';
  }
  if (['小吉', '吉', '稳步前进', '健康第一', '财源广进'].includes(type)) {
    return '尚可';
  }
  if (['末吉', '调整方向', '温故知新', '稳中求变'].includes(type)) {
    return '平平';
  }
  if (['小凶', '凶', '大凶'].includes(type)) {
    return '不佳';
  }
  return '一般';
}; 